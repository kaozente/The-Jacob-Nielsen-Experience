#include "DepthCameraException.h"

#include <iostream>

////////////////////////////////////////////////////////////////////////////////
//
// DepthCameraException
//
////////////////////////////////////////////////////////////////////////////////

DepthCameraException::DepthCameraException(std::string message) throw()
{
	m_message = message;

	std::cerr << "[Error] " << message << std::endl;
}

////////////////////////////////////////////////////////////////////////////////

DepthCameraException::~DepthCameraException() throw()
{

}

////////////////////////////////////////////////////////////////////////////////

const char* DepthCameraException::what() const throw()
{
	return m_message.c_str();
}
